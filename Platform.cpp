Platform::Platform(const glm::vec3 &pos, const glm::vec3 &size) {
    position = pos;
    this->size = size;
    model = glm::translate(glm::mat4(1.0f), position);
    model = glm::scale(model, size);
    initVertices();
    initBuffers();
    initShader();
}

void Platform::draw(const glm::mat4 &view, const glm::mat4 &projection) const {
    shader.useProgram();
    shader.changeUniform4M("view", view);
    shader.changeUniform4M("projection", projection);
    shader.changeUniform4M("model", model);
    glBindVertexArray(VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

void Platform::initVertices() {
    for(int i = 0; i < 216; i++){
        vertices[i] = vertexData[i];
    }
}

void Platform::initBuffers() {
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(float)*6, (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, sizeof(float)*6, (void*)0);
    glEnableVertexAttribArray(1);
}

void Platform::initShader() {
    shader.loadVertex("../Platform.vert");
    shader.loadFragment("../Platform.frag");
    shader.createProgram();
    shader.useProgram();
}

const glm::vec3 &Platform::getSize() const {
    return size;
}

const glm::vec3 &Platform::getPosition() const {
    return position;
}


